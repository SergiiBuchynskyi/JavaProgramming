package day03_EscapeSequence;

public class EscapeSequence {
    public static void main(String[] args) {
/*
    \n: starts a new line
    \t: paragrph space(tab)
    \\: single back slash
    \": double quote
 */

        System.out.println("Java \nPython\nC#");

        System.out.println("________________");

        System.out.println("Hello Cydeo \nHow are you all today?\nIt's nice to see you");

        System.out.println("\tJava is cool");

        System.out.println("/ \\");

        System.out.println("My favorite show is \"Game of throne");
    }
}
