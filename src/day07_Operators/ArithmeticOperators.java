package day07_Operators;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 3;

        int division = num1/num2;
        int reminder = num1%num2;
        //10 divide by 3 is =3 with %1
        System.out.println(num1 + " devided by " + num2 + " is equal to " + division + " with a remainder of " + reminder);
    }
}
