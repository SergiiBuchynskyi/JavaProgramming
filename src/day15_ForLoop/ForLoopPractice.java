package day15_ForLoop;

public class ForLoopPractice {
    public static void main(String[] args) {

        for (int i = 15; i<=45; i++){
            System.out.print(i + " ");
        }

        System.out.println();

        for (int j = 100; j>=50; j--){
            System.out.print(j + " ");
        }
        System.out.println();

        for (int q = 2; q <= 54; q+=2){
            System.out.println(q + " ");
        }


    }
}
